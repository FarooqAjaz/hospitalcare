@Composable
fun HospitalCareAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    language: String = "en",
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {

    val layoutDirection = if (language == "ur") LayoutDirection.Rtl else LayoutDirection.Ltr
    val shadowsColor = if (darkTheme) md_theme_dark_shadow else md_theme_light_shadow

    val shadows = Shadows(
        light = Shadow(1.dp, color = shadowsColor),
        medium = Shadow(2.dp, color = shadowsColor),
        dark = Shadow(3.dp, color = shadowsColor),
    )
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    CompositionLocalProvider(
        LocalShadows provides shadows,
        LocalLayoutDirection provides layoutDirection
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            content = content
        )
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

@Composable
fun StatusBarColor(color: Color) {
    val view = LocalView.current
    val darkTheme = isSystemInDarkTheme()

    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = color.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }
}